#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;
	long	nb1;

	nb1 = nb;
	if (nb1 < 0)
	{
		write(1, "-", 1);
		nb1 = -nb1;
	}
	if (nb1 >= 10)
		ft_putnbr(nb1 / 10);
	c = '0' + nb1 % 10;
	write(1, &c, 1);
}

