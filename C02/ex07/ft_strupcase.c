char	*ft_strupcase(char *str)
{
	char	*original_str;

	original_str = str;
	while (*str != '\0')
	{
		if (*str > 96 && *str < 123)
		{
			*str = *str - 32;
		}
		str++;
	}
	return (original_str);
}
